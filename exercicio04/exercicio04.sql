SELECT PE.CODPESSOA, PE.NOME, PE.CPF, PE.TELEFONE, CI.NOMECIDADE, ES.UF
FROM ESTADO ES
INNER JOIN CIDADE CI ON ES.CODESTADO = CI.CODESTADO
INNER JOIN ENDERECO EN ON CI.CODCIDADE = EN.CODCIDADE
INNER JOIN ENDERECO_PESSOA EP ON EN.CODENDERECO = EP.CODENDERECO
INNER JOIN PESSOA PE ON EP.CODPESSOA = PE.CODPESSOA
ORDER BY ES.UF DESC, CI.NOMECIDADE 


SELECT FU.CODFUNCIONARIO, CA.NOMECARGO, CA.SALARIOBASE, CA.NIVEL, FU.TOTALHORASEXTRAS, FP.MESREFERENCIA, FP.SALARIOLIQUIDO, FP.DESCONTO_INSS
FROM CARGO CA
INNER JOIN FUNCIONARIO FU ON CA.CODCARGO = FU.CODFUNCIONARIO
INNER JOIN FOLHAPAGAMENTO FP ON FU.CODFUNCIONARIO = FP.CODFUNCIONARIO
ORDER BY FP.MESREFERENCIA, FU.CODFUNCIONARIO


SELECT PE.NOME, DF.DATAADMISSAO, CA.NOMECARGO , DE.NOMEDEPARTAMENTO, CI.NOMECIDADE, ES.UF 
FROM ESTADO ES
INNER JOIN CIDADE CI ON ES.CODESTADO = CI.CODESTADO
INNER JOIN ENDERECO EN ON CI.CODCIDADE = EN.CODCIDADE
INNER JOIN ENDERECO_PESSOA EP ON EN.CODENDERECO = EP.CODENDERECO
INNER JOIN PESSOA PE ON EP.CODPESSOA = PE.CODPESSOA
INNER JOIN FUNCIONARIO FU ON PE.CODPESSOA = FU.CODFUNCIONARIO
INNER JOIN CARGO CA ON FU.CODCARGO = CA.CODCARGO
INNER JOIN DEPARTAMENTO_FUNCIONARIO DF ON FU.CODFUNCIONARIO = DF.CODFUNCIONARIO
INNER JOIN DEPARTAMENTO DE ON DF.CODDEPARTAMENTO = DE.CODDEPARTAMENTO
ORDER BY CA.NOMECARGO
