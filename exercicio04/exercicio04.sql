CREATE TABLE ESTADO(
	CODESTADO INT PRIMARY KEY IDENTITY(1,1),
	NOMEESTADO NVARCHAR(50),
	UF NVARCHAR(2)
)

CREATE TABLE CIDADE(
	CODCIDADE INT PRIMARY KEY IDENTITY(1,1),
	CODESTADO INT,
	NOMECIDADE NVARCHAR(200)
)

CREATE TABLE ENDERECO(
	CODENDERECO INT PRIMARY KEY IDENTITY(1,1),
	CODCIDADE INT,
	CEP NVARCHAR(8),
	LOGRADOURO NVARCHAR(200),
	BAIRRO NVARCHAR(200),
	NUM NVARCHAR(10),
	COMPLEMENTO NVARCHAR(200)
)

CREATE TABLE ENDERECO_PESSOA(
	CODPESSOA INT NOT NULL,
	CODENDERECO INT NOT NULL,
	DATACADASTRO DATE,
	STATUS NVARCHAR(10)
)

CREATE TABLE PESSOA(
	CODPESSOA INT PRIMARY KEY IDENTITY(1,1),
	NOME NVARCHAR(200),
	CPF NVARCHAR(11),
	TELEFONE NVARCHAR(11),
	EMAIL NVARCHAR(200),
	SEXO NVARCHAR(1),
	DATANASCIMENTO DATE,
	DATACADASTRO DATE
)

CREATE TABLE FUNCIONARIO(
	CODFUNCIONARIO INT PRIMARY KEY,
	CODCARGO INT,
	TOTALHORASEXTRAS INT
)

CREATE TABLE CARGO(
	CODCARGO INT PRIMARY KEY IDENTITY(1,1),
	NOMECARGO NVARCHAR(100),
	SALARIOBASE DECIMAL(7,2),
	NIVEL NVARCHAR(100)
)

CREATE TABLE FILIAL(
	CODFILIAL INT PRIMARY KEY IDENTITY(1,1),
	CODENDERECO INT,
	CNPJ NVARCHAR(14),
	TELEFONE NVARCHAR(11),
	EMAIL NVARCHAR(100)
)

CREATE TABLE DEPARTAMENTO(
	CODDEPARTAMENTO INT PRIMARY KEY IDENTITY(1,1),
	NOMEDEPARTAMENTO NVARCHAR(50)
)

CREATE TABLE DEPARTAMENTO_FUNCIONARIO(
	CODDEPARTAMENTO INT NOT NULL,
	CODFUNCIONARIO INT NOT NULL,
	DATAADMISSAO DATE
)

CREATE TABLE FOLHAPAGAMENTO(
	CODIGO INT PRIMARY KEY IDENTITY(1,1),
	CODFUNCIONARIO INT,
	MESREFERENCIA NVARCHAR(2),
	SALARIOLIQUIDO DECIMAL(7,2),
	VALORHORAEXTRA DECIMAL(7,2),
	DESCONTO_INSS DECIMAL(7,2)
)

CREATE TABLE FILIAL_DEPARTAMENTO(
	CODFILIAL INT NOT NULL,
	CODDEPARTAMENTO INT NOT NULL,
	DATACADASTRO DATE,
	STATUS NVARCHAR(10)
)

